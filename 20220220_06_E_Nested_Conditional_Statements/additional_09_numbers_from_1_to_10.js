function numbers1To10() {
    let i = 1;
    while (i <= 10) {
        console.log(i);
        i++;
    }
}
numbers1To10();


// ------------- done with "for" loop -----------------------


function numbersFrom1To10() {
    for (let i = 1; i <= 10; i++) {
        console.log(`${i}`);
    }
}
numbersFrom1To10();
